//For loop

let key = 10;
for (let i = 1; i < key; i++) {
  console.log(i);
}

//While loop
let value = 5;
while (value != 0) {
  console.log(value);
  value--;
}

//do While loop
let val = 4
do{
    console.log(val)
    val--
}
while(val != 0);

